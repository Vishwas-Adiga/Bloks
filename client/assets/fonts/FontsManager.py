import pygame
pygame.font.init()

def productSansRegular(size):
    return pygame.font.Font('client/assets/fonts/Product Sans Regular.ttf', size)

def productSansItalic(size):
    return pygame.font.Font('client/assets/fonts/Product Sans Italic.ttf', size)

def productSansBold(size):
    return pygame.font.Font('client/assets/fonts/Product Sans Bold.ttf', size)

def productSansBoldItalic(size):
    return pygame.font.Font('client/assets/fonts/Product Sans Bold Italic.ttf', size)
